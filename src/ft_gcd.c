#include "../inc/libft.h"

int ft_gcd(int x, int y)
{
	int g;

	x = ABS(x);
	y = ABS(y);
	if (x == 0 && y == 0)
		return (-1);
	g = y;
	while (x > 0)
	{
		g = x;
		x = y % x;
		y = g;
	}
	return (g);
}
